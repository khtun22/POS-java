    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        if(Checking.IsValidName(txtcurname.getText()))
        {
            JOptionPane.showMessageDialog(null, "Please enter VALID Name.");
            txtcurname.requestFocus();
            txtcurname.selectAll();
        }
        else if(Checking.IsNull(txtadd.getText()))
        {
            JOptionPane.showMessageDialog(null,"Please enter Address.");;
            txtadd.requestFocus();
            txtadd.selectAll();
        }
        else if(Checking.IsNull(txtph.getText()))
        {
            JOptionPane.showMessageDialog(null, "Please enter Phone.");
            txtph.requestFocus();
            txtph.selectAll();
        }
        else if(!Checking.IsAllDigit(txtph.getText()))
        {
            JOptionPane.showMessageDialog(null,"Please enter valid Phone Number.");
            txtph.requestFocus();
            txtph.selectAll();
        }
        else if(Checking.IsNull(txtemail.getText()))
        {
            JOptionPane.showMessageDialog(null, "Please enter NRC.");
            txtemail.requestFocus();
            txtemail.selectAll();
        }

        else
        {
            String str[]=new String[5];
            String id = lblsid.getText();
            str[0]=txtcurname.getText();
            str[1]=txtadd.getText();
            str[2]=txtph.getText();
            str[3]=txtemail.getText();
//            str[4]=txtun.getText();
     //       str[5]=txtpassword.getText();
            str[4]=Checking.checkboxche(chbpur,chbs,chbsf);
            System.out.println(id);
            boolean save = msql.updateRecord("staffed", id, str);
            if(save)
            {
                JOptionPane.showMessageDialog(null, "Successfully update record!","Update Record.",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Failed to save record","Cannot Save",JOptionPane.INFORMATION_MESSAGE);
            }
            clear();
            lblsid.requestFocus();
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        if(lblsid.getText().equals(null))
        {
            JOptionPane.showMessageDialog(null, "Please choose Staff ID!");
            lblsid.requestFocus();
        }
        else
        {
            if(JOptionPane.showConfirmDialog(null, "Are you sure you want to DELETE this staff?Once done,there is no rolling back action!","Confirm Delete",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)==JOptionPane.YES_OPTION)
            {
                try
                {
                    String id = lblsid.getText();
                    clear();

                    msql.deleteRecord("staff", id);
                    //fillCustomer();
                }
                catch(Exception sqle)
                {
                    sqle.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

