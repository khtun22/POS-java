    public void permission()
    {
        if(strs!=null)
        {
            int j=0;
            j=strs.length();
            char a[]=new char[j];
            for(int k=0;k<strs.length();k++)
            {
                a[k]=strs.charAt(k);
                loop(a[k]);
            }
        }
    }
    public void loop(char c)
    {
        
            if(c=='P')
            {
                chbpur.setSelected(true);
            }
            if(c=='S')
            {
                chbs.setSelected(true);
            }
            if(c=='N')
            {
                chbsf.setSelected(true);
            }
        
    }
    
