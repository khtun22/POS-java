    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        if(txttid.getText().equals(null)) {
            JOptionPane.showMessageDialog(this,"Please choose Type ID");
            txttid.requestFocus();
        } else if(Checking.IsNull(txtname.getText())) {
            JOptionPane.showMessageDialog(null,"Please enter Type name.");
            txtname.requestFocus();
            txtname.selectAll();
        }

        else {
            String st[]=new String[2];
            st[0]=(String)txtname.getText();
            st[1]=(String)txttid.getText();
            boolean br = msql.isduplicate("type", st);
            if(!br) {
                JOptionPane.showMessageDialog(null,"Duplicate Record!");
            } else {
                String[]str=new String[1];
                str[0]=txtname.getText();

                String id = txttid.getText();

                boolean save = msql.updateRecord("type", id, str);
                if(save) {
                    JOptionPane.showMessageDialog(null,"Successfully updated record!","Updated Record.",JOptionPane.INFORMATION_MESSAGE);
                    clear();
                    txttid.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null,"Failed to update the specified record","Cannot Update.",JOptionPane.INFORMATION_MESSAGE);
                    clear();
                    txttid.requestFocus();

                }
                clear();
                txttid.requestFocus();
            }
        }
    }//GEN-LAST:event_btnupdateActionPerformed

