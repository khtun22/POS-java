    private void txtmonthKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtmonthKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
            if(txtmonth.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Please choose Month");
                txtmonth.requestFocus();
            } else {
                String str = "select * from purchase where Month(purchasedate)='"+txtmonth.getText()+"' ORDER BY Day(purchasedate) ;";
                fillPurData(str);
            }
        }
    }//GEN-LAST:event_txtmonthKeyPressed

