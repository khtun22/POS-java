    public void fillPurData(String sql)
    {
        String strdataitem[]=new String[5];
        String strquery[]=new String[6];
        String dates = null;
        try
        {
            Statement ste = con.createStatement();
            while(dtm.getRowCount()>0)
                dtm.removeRow(0);
           ResultSet rs = ste.executeQuery(sql) ;
           while(rs.next())
           {
               strdataitem[0]=rs.getString(1);//purchaseid
               
               strquery =  msql.getstaffdata(rs.getString(4));//supplierid to supplierdata
               strdataitem[1]=strquery[0];//suppliername
               strquery = msql.getSupplierData(rs.getString(2));//itemid to itemdata
               strdataitem[2]=strquery[1];//itemname
               strdataitem[3]=rs.getString(3);//price
               dates=strdataitem[3];
               strdataitem[3]=dates.substring(0, 10);
               strdataitem[4]=rs.getString(5);//total amount
              
               dtm.addRow(strdataitem);


           }
           tblpurchase2.setModel(dtm);
        }
        catch(SQLException sqle)
        {
            System.out.println(sqle);
        }
    }

